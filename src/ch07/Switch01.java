package campus.ch07;

class Switch01 {

    public static void main(String[] args) {

        //값을 통해 불러오는 대상을 정의
        switch (10) {
            case 10:
                System.out.println("동작 10");
                break;
            case 5:
                System.out.println("동작 5");
                break;
            case 3:
                System.out.println("동작 3");
                break;
        }

    }
}
